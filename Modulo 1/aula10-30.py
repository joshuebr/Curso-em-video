print('\nDesafio 30\n\nIdentificando se o número é par ou ímpar.\n')
while True:
    numero = int(input('Digite um número:'))
    if (numero % 2) == 0:
        print('\nO número digitado foi par!\n\nFIM!!\n')
        break
    else:
        print('\nO número digitado foi impar!\n\nFIM!!!\n ')
        break